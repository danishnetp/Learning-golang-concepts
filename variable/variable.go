package main

import "fmt"

func main() {
	// With type
	var name string = "golang1"
	fmt.Println(name)

	// Without type
	var name1 = "golang2"
	fmt.Println(name1)

	// Short hand declaration
	name2 := "golang3"
	price := 100
	fmt.Println(name2)
	fmt.Println(price)

	// boolean type
	var isTrue bool = true
	fmt.Println(isTrue)
}
